        <div class="col-lg-5 col-md-9 col-sm-12">
            <p class="fs-5">
                The City of Taguig entered into two major agreements with key partners on Friday, August 16, 2024, at the 
                Grand Hyatt Hotel in BGC, Fort Bonifacio. The agreements aim to enhance healthcare accessibility and improve programs 
                for the health of mothers and children in the City. 
                <br><br>
                The first initiative, led by CareSpan Asia Inc., in partnership with Temasek Foundation, is a pilot program that brings 
                together multiple stakeholders in a Public-Private-Philanthropic Partnership (PPPP) to onboard 350,000 underserved 
                citizens in Taguig City. Under this partnership, CareSpan will provide Taguig City with access to its advanced Digital Health care platform. 
                <br><br>
                This platform, which is composed of an electronic medical records (EMR) system and telemedicine capabilities, will be integrated into 
                Taguig's local healthcare program to ensure that target beneficiaries have access to quality medical services.
                <br><br>
                Singapore’s Temasek Foundation supports this initiative with a funding of S$2.12 million to ensure the program’s sustainability and potential 
                scalability to other localities. For its part, the City of Taguig will train volunteers and health workers to engage 
                low-income communities, promoting greater health awareness and encouraging enrollment in UHC.
            </p>

            <p class="fs-5">
                The City of Taguig actively took part in the third quarter National Simultaneous Earthquake Drill 
                (NSED) to enhance disaster preparedness and resilience.
                <br><br>
                Government and private employees, uniformed personnel, students, and residents practiced the “Duck, Cover, and Hold” technique, 
                as well as how to reach their designated evacuation sites and apply other safety measures in response to an earthquake.
                The NSED was successfully conducted across public schools, offices, barangays, and city-owned buildings and facilities.
                <br><br>
                Rescue teams showcased proper procedures for assisting trapped individuals and those with injuries during a simulated rescue operation.
                <br><br>
                The City also deployed its state-of-the-art Mobile Control and Command Vehicle (MOCCOV), a vital tool in disaster management that allows 
                for rapid coordination of resources and personnel during emergencies.
                <br><br>
                Under the leadership of Mayor Lani Cayetano, Taguig is committed to building a safer and more resilient community by actively participating 
                in all National Simultaneous Earthquake Drills and promoting the importance of preparedness and teamwork in responding to disasters.

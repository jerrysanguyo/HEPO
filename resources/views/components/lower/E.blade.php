            <p class="fs-5">
                In celebration of World No Tobacco Day and National No Smoking Month, the City of Taguig gathered more 
                than 220 members of the Taguig Smoke-Free Task Force, representatives from the City Health Office, youth sector, 
                and vape business owners at Lakeshore Hall in Barangay Lower Bicutan on Wednesday, June 5.
                <br><br>
                With the theme “Protecting Children from Tobacco Industry Interference", the event aims to strengthen the City’s 
                tobacco control program under its Transformative, Lively, and Caring City agenda. As part of the celebration, 
                Taguig Mayor Lani Cayetano led the destruction of confiscated cigarettes and e-cigarettes/vapes, symbolizing Taguig’s 
                commitment to a smoke-free environment.
                <br><br>
                The celebration adopted a multi-sectoral approach to achieving a smoke-free Taguig where CHO conducted lectures on Taguig’s 
                tobacco control program, TB program, lifestyle-related diseases, and mental health. These lectures tackled the adverse effects 
                of tobacco and vape consumption and highlighted the overall benefits to health and well being of not engaging in or quitting these products. 

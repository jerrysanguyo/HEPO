            <p class="fs-5">
                As part of the annual observance of National Deworming Month and Global Handwashing Day, the City of Taguig, 
                through its City Health Office (CHO) and in partnership with DEPED Tapat, held an educational session on proper 
                handwashing and deworming for children at Tenement Elementary School on Tuesday, October 14, 2024.
                <br><br>
                Representatives from the Technical Education and Skills Development Authority (TESDA) Women’s Center and companies, 
                such as HCG Philippines and Unitec, were present to underscore the importance of these health practices for children’s well-being.
                <br><br>
                The event gathered 51 parents, 300 students from Grades 1 to 6, and teachers from Tenement Elementary School.
                <br><br>
                With the theme "Goodbye Bulate: Sabayang Pagpupurga Kontra Bulate," the seminar focused on the critical role of early deworming 
                in preventing child malnutrition and parasitic worm-related disorders. It also highlighted handwashing as the first line 
                of defense against pathogens.
            </p>

        <div class="col-lg-5 col-md-9 col-sm-12">
            <p class="fs-5">
                The City Nutrition Office, in coordination with the City Health Office and the Taguig City Health Wellness team, launched the Kiddie Wellness 
                Program at the open space near Lakeshore Hall in Barangay Lower Bicutan on September 14. 
                <br><br>
                Themed "Tara Push Bike, Tayo!," the event is a key component 
                of Taguig’s 2024 Overweight and Obesity Management and Prevention Program for Children. More than 50 children, aged 3 to 5, joined the program 
                with their parents or legal guardians. 
                <br><br>
                This initiative seeks to engage children in recreational and physical activities that help manage 
                body weight while ensuring they enjoy the process. Biking was introduced as a recommended activity, with the children and their parents 
                receiving push bike kits, including helmets, kiddie bikes, knee pads, and a token bag with fruits, a tumbler, a towel, a probiotic drink, 
                oatmeal, and other essential items. 
                <br><br>
                The event also featured a demonstration on riding push bikes, ensuring the children 
                learned proper techniques. To ensure safety, a first-aid station was available on site for the participants. 
                <br><br>
                Parents were also given guidance on 
                the importance of nutritious food and frequent outdoor exercise to foster a healthy lifestyle and bond with their children. 
            </p>
